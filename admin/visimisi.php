Visi dan Misi</h1>
<p>
<b>Visi :</b><br>
Terwujudnya  warga sekolah yang religius, cerdas, disiplin, jujur, bersahabat dan peduli  lingkungan.<br>
<b>Misi :</b><br>
  1.	Menumbuh kembangkan suasana dan kegiatan keagamaan yang dimulai dengan shalat berjamaah .<br>
  2.	Membudayakan kebiasaan berlaku jujur didlam maupun diluar lingkungan sekolah.<br>
  3.	Mengimplementasikan kedisiplinan dalam kehidupan sehari-hari.<br>
  4.	Membina keakraban dan persaudaraan antar sesama warga sekolah.<br>
  5.	Mengembangkan rasa cinta dan kepedulian terhadap lingkungan<br>
6.	Menghasilkan lulusan cerdas dan berkarakter yang dapat diterima dalam kehidupan bermasyarakat serta dapat melanjutkan keperguruan tinggi negeri yang favorit.</p>
<p><br>
  
  <b>Tujuan :</b></br>
</p>
<p> 1.	Menanamkan nilai-nilai agama dan berakhlak mulia.<br>
  2.	Terciptanya kedisiplinan warga sekolah secara menyeluruh.<br>
  3.	Mengembangkan budaya jujur,keterbukaan dan bertanggung jawab dalam proses pendidikan dan kehidupan sehari-hari.<br>
  4.	Berpartisipasi aktif dalam memajukan dan memelihara kelestarian lingkungan.<br>
  5.	Meningkatkan kreatifitas siswa dibidang ilmu pengetahuan,teknologi,seni,budaya dan olahraga.<br>
  6.	Meningkatkan jumlah lulusan yang diterima diperguruan tinggi negeri dan swasta favorit.<br>
  7.	Meningkatkan jumlah lulusan yang diterima diperguruan tinggi negeri dan swasta favorit.<br>
  8.	Terciptanya disiplin warga sekolah secara menyeluruh.<br>
  9.	Terlaksananya proses belajar mengajar yang aktif,kreatif,inovatif dan menyenangkan.<br>
  10.	Tercapainya rata-rata nilai hasil UN 2015 (rata-rata&gt;8,00).<br>
  11.	Tercapainya siswa diterima diperguruan tinggi 80% tahun 2015.<br>
  12.	Tercapainya sekolah menjadi sekolah adiwiyata nasional tahun 2015.<br>
  
</p>
